# and
x = 5
y = 10
print(x > 3 and y < 20)
print(x > 6 and y < 20)

# or
x = 5
y = 10
print(x > 3 or y > 20)  
print(x > 6 or y > 20) 

# not
x = 5
print(not x > 3)  
print(not x < 3)

#in e not in
frutas = ["maçã", "banana", "laranja"]
print("maçã" in frutas)
print("maçã" not in frutas)



# Combinação de operadores lógicos
