#Variáveis em Python

#Em **Python**, uma variável é um nome que **armazena um valor**. Você pode pensar em uma variável como uma **caixa** onde você guarda um valor, como um número ou uma string, e você pode acessar esse valor mais tarde ao chamar o nome da variável.

#Em Python, a **declaração de uma variável** não exige a palavra-chave `var` ou tipo explícito. Você só precisa atribuir um valor a um nome de variável usando o operador `=`.

nome = "Ana"
idade = 23
altura = 1.80
esta_estudando = True

nome = "Maria"
sobrenome = "Silva"

nome_completo = nome + " " + sobrenome  # "Maria Silva"