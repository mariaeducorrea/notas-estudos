#Adição
a = 10
b = 5
print(a + b)  # 15

#Subtração
print(a - b)  # 5

#Multiplicação
print(a * b)  # 50

#Divisão
print(a / b)  # 2.0

#Divisão inteira
print(a // b)  # 2

#Modulo
print(a % b)  # 0

#Exponenciação 
print(a ** 2)  # 100 (10²)

#Operadores Unários
# Operador unário negativo
negativo = -a
print(negativo)  # -10

# Operador unário positivo (não altera o valor)
positivo = +a
print(positivo)  # 10