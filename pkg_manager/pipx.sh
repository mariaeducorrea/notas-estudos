#PIPX

# pipx é uma ferramenta para instalar e executar aplicativos Python em ambientes virtuais isolados
# Com o pipx, você pode instalar aplicativos Python globalmente, mas cada um deles roda em seu 
#próprio ambiente virtual, evitando conflitos entre pacotes e versões.


# Instalação do pipx
pip install pipx

# Instalar um Aplicativo
pipx install nome_do_pacote

# Listar Aplicativos Instalados
pipx list

# Desinstalar um Aplicativo
pipx uninstall nome_do_pacote

# Atualizar um Aplicativo
pipx upgrade nome_do_pacote


